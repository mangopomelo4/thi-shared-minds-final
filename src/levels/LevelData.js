/**
 * LevelData — All 5 level definitions.
 *
 * Tile legend:
 *   0 = air
 *   1 = solid ground
 *   2 = solid wall
 *   3 = placeable zone (Level 1 — Player 2 can place blocks here)
 *   4 = letter block (Level 2)
 *   5 = blink platform (Level 3)
 *   9 = exit trigger
 *
 * Map is stored row-major. Each level is 60 cols × 17 rows (960×272 px).
 */

const COLS = 60;
const ROWS = 17;

function makeMap(strings) {
  const tiles = [];
  for (const row of strings) {
    for (let c = 0; c < COLS; c++) {
      tiles.push(parseInt(row[c] || '0', 10));
    }
  }
  return tiles;
}

